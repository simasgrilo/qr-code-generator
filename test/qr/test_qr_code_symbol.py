class TestQRCodeSymbolVer6(unittest.TestCase):
    """ Test class to perform tests with QR Code Version 6"""
    def setUp(self):
        self._qr = QRCodeImage(7)

    def test_qr_code_alignemnt_multiple(self):
        centers = [(6, 22), (22, 6), (22, 22), (22, 38), (38, 22)]
        self._qr.position_alignment_pattern()
        for row, col in centers:
            self.assertTrue(self._check_alignment_patterns(row, col))

    def _check_alignment_patterns(self, row: int, col: int):
        """Method to check if all required cells by the alignment patterns are filled according with the
           QR code version.
           This is essentially the negated idea of the check_overlap method in class QRCodeImage

        Args:
            row (int): row number of the center module
            col (int): col number of the center module

        Returns:
            _type_: _description_
        """
        qr_matrix = self._qr.get_matrix()
        if qr_matrix[row][col] != 1:
            return False
        # consider the square edges as the starting verification points:
        top_left = (row - 2, col - 2) # goes right
        top_right = (row - 2, col + 2) # goes down
        bot_left = (row + 2, col - 2) # goes up
        bot_right = (row + 2, col + 2) # goes left
        for offset in range(5):
            if (not qr_matrix[top_left[0]][top_left[1] + offset] or
                not qr_matrix[top_right[0] + offset][top_right[1]] or
                not qr_matrix[bot_left[0] - offset][bot_left[1]] or
                not qr_matrix[bot_right[0]][bot_right[1] - offset]):
                return False
        return True